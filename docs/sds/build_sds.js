// paragraph from a string, or from an array of {text, bold} runs
function P(input, opts = {}) {
  const runs = Array.isArray(input)
    ? input.map((r) => new TextRun({ text: r.text, bold: !!r.bold, italics: !!r.italics }))
    : [new TextRun(input)];
  return new Paragraph({
    children: runs,
    spacing: { after: 160, line: 276 },
    alignment: opts.justify === false ? undefined : AlignmentType.JUSTIFIED,
  });
}

function bullet(t) {
  return new Paragraph({ numbering: { reference: 'b', level: 0 }, spacing: { after: 60, line: 264 },
    children: [new TextRun(t)] });
}

const border = { style: BorderStyle.SINGLE, size: 1, color: 'CBD5E1' };
